package aula06exercicios;

public class Calculadora {

    int somar(int a, int b) {
        return a + b;
    }

    int subtrair(int a, int b) {
        return a - b;
    }

    int multiplicar(int a, int b) {
        return a * b;
    }

    double dividir(int a, int b) {
        return (double) a / b;
    }
}